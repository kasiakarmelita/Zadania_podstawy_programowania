package pl.Karmelita;

public class Zad15Number {
    private int value;
    private int counter;

    public Zad15Number(int value, int counter) {
        this.value = value;
        this.counter = counter;
    }

    public int getValue() {

        return value;
    }

    public int getCounter() {
        return counter;

    }

    public void setCounter(int counter) {

        this.counter = counter;
    }
}
